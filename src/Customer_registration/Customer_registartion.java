    private void SubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButtonActionPerformed
                                                  
    String firstname = FirstNameTextField.getText();
    String lastname = LastNameTextField.getText();
    String age = AgeTextField.getText();
    String email = EmailTextField.getText();
    String message = messageareatext.getText();

    if (firstname.equals("") || lastname.equals("") || age.equals("") || email.equals("")) {
        JOptionPane.showMessageDialog(null, "All the fields are mandatory");
    } else {
        if (selectedImageFile != null) {
            try {
                // Load the selected image file
                BufferedImage img = ImageIO.read(selectedImageFile);
                ImageIcon icon = new ImageIcon(img);

                // Display customer details and the selected image in a JOptionPane dialog
                JOptionPane.showMessageDialog(this,
                        "CUSTOMER NAME: " + firstname + " " + lastname + "\n" +
                                "AGE: " + age + "\n" +
                                "EMAIL ID: " + email + "\n" +
                                "MESSAGE: " + message,
                        "CUSTOMER DETAILS", JOptionPane.INFORMATION_MESSAGE, icon);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error loading the image.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please upload a photo before submitting.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_SubmitButtonActionPerformed

