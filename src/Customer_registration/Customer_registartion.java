    private void uploadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadButtonActionPerformed
        // TODO add your handling code here:                                            
    JFileChooser fileChooser = new JFileChooser();
    int returnValue = fileChooser.showOpenDialog(null);

    if (returnValue == JFileChooser.APPROVE_OPTION) {
        selectedImageFile = fileChooser.getSelectedFile();
        try {
            // Load the selected image file
            BufferedImage img = ImageIO.read(selectedImageFile);
            ImageIcon icon = new ImageIcon(img);

            // Display the selected image in a JLabel
            // Assuming you have a JLabel component named "imageLabel" in your UI
            imageLabel.setIcon(icon);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error loading the image.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_uploadButtonActionPerformed

